#Variables
number1 = 10
number2 = 5

#Addition
sum_add = number2 + number1
print(f"Addition of 10 and 5 is {sum_add}")

#Subtraction
sum_sub = number1 - number2
print(f"Subtraction of 10 and 5 is {sum_sub}")

#multiplication
sum_multi = number2 * number1
print(f"Multiplication of 10 and 5 is {sum_multi}")